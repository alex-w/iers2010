      PROGRAM MAIN

      DOUBLE PRECISION T, MJD, MIN_MJD, MAX_MJD, DUT, DLOD, DOM, JC
      INTEGER NUM_TESTS, I,J

      NUM_TESTS = 1000
      MIN_MJD = 47892D0
      MAX_MJD = 66154D0

      DO I=1,NUM_TESTS-1
        CALL RANDOM_NUMBER(T)
        MJD = (MAX_MJD-MIN_MJD)*T + MIN_MJD
        JC = (MJD - 51544.5D0)/36525D0
        CALL RG_ZONT2(JC, DUT, DLOD, DOM)
        WRITE(*,'(E30.22,X,E25.17,X,E25.17,X,E25.17)') 
     .  MJD, DUT, DLOD, DOM
      END DO

      END
